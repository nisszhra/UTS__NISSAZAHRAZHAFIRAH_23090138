bil_pertama = int(input('Masukan bilangan pertama : '))
bil_kedua = int(input('Masukan bilangan kedua : '))

penjumlahan = bil_pertama + bil_kedua
pengurangan = bil_pertama - bil_kedua
modulus = bil_pertama % bil_kedua

print(f'Hasil penjumlahan = {penjumlahan}')
print(f'Hasil pengurangan = {pengurangan}')
print(f'Hasil sisa bagi = {modulus}')